def get_industry_insight(industry: str) -> dict:
    """获取行业解读信息

    Args:
        industry: Tushare stock_basic 的 industry 字段值（如"铜"、"电气设备"）

    Returns:
        {"tags": [...], "one_line": "...", "drivers": [...], "risks": [...], "related_policy": "..."}
        如果没有模板，返回空 dict
    """
    return INDUSTRY_TEMPLATES.get(industry, {})


def get_fund_industry(fund_name: str) -> dict:
    """从基金名称推断行业分类

    Args:
        fund_name: 基金名称（如"华夏科创50ETF联接A"）

    Returns:
        {"tag": "🤖 AI/科技", "desc": "AI+半导体+软件，高弹性高波动"}
        如果匹配不到返回空 dict
    """
    for keywords, tag, desc in FUND_INDUSTRY_MAP:
        if any(kw in fund_name for kw in keywords):
            return {"tag": tag, "desc": desc}
    return {}


def enrich_stock_with_industry(stock: dict) -> dict:
    """给单只股票补充行业解读字段"""
    industry = stock.get("industry", "")
    if not industry:
        return stock

    insight = get_industry_insight(industry)
    if insight:
        stock["industry_tags"] = insight.get("tags", [])
        stock["industry_insight"] = insight.get("one_line", "")
        stock["industry_drivers"] = insight.get("drivers", [])[:3]
        stock["industry_risks"] = insight.get("risks", [])[:2]
    return stock


def enrich_fund_with_industry(fund: dict) -> dict:
    """给单只基金补充行业分类标签"""
    name = fund.get("name", "")
    if not name:
        return fund

    match = get_fund_industry(name)
    if match:
        fund["industry_tag"] = match["tag"]
        fund["industry_desc"] = match["desc"]
    return fund
